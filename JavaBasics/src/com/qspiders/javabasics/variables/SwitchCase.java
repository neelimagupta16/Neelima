package com.qspiders.javabasics.variables;

public class SwitchCase {
	
	
	public static void main(String[] arg)
	{
		String grade= "B";

		
		switch(grade)
		{
		case "A+":
			
			System.out.println("Grade is: " +grade);
			break;
			
		
		case "A":
			System.out.println("Grade is: " +grade);
			break;
			
		case "B+":
			
			System.out.println("Grade is: " +grade);
			break;
			
			case "B":
			System.out.println("Grade is: " +grade);
			break;
			
	}

}}
